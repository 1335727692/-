public class LinkWord {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String a1 = scanner.nextLine();
        a1 = a1.trim();
        String a2 = scanner.nextLine();
        a2 = a2.trim();
        System.out.println(a1 + a2);
    }
}
